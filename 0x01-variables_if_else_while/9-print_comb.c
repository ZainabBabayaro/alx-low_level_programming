#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
 * main - Determines if the number is positive, negative or zero
 * Return: Always 0 (Success)
 */
int main(void)
{
	int n;

	for (n = 48; n < 58; n++)
	{
		putchar(n);
		if (n != 57)
		{
			putchar(',');
			putchar(' ');
		}
	}
	putchar('\n');
	return (0);
}
